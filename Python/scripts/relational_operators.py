g = 20

# Equavalence ==
print(g == 20)
print(g == 3)

# Ä°nequivalence!=
print(g != 20)
print(g != 3)

# Less than <
print(g < 3)
print(g < 23)

# Greater than >
print(g > 3)
print(g > 23)

# Less than or equal to <=
print(g <= 3)
print(g <= 20)

# Greater than or equal to >=
print(g >= 3)
print(g >= 20)